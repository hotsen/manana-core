class Item < ApplicationRecord
  #
  #
  # Concerns
  # --------------------------------------------------------------------------------------
  #
  #
  #

  include Codified

  #
  #
  # Plugins
  # --------------------------------------------------------------------------------------
  #
  #
  #


  #
  #
  # Associations
  # --------------------------------------------------------------------------------------
  #
  #
  #

  belongs_to :account

  #
  #
  # Scopes
  # --------------------------------------------------------------------------------------
  #
  #
  #


  #
  #
  # Callbacks
  # --------------------------------------------------------------------------------------
  #
  #
  #

  before_validation :ensure_uuid, on: :create
  before_validation :ensure_mood, on: :create

  #
  #
  # Validations
  # --------------------------------------------------------------------------------------
  #
  #
  #

  validates :name, presence: true
  validates :uuid, presence: true, uniqueness: true
  validates :mood, presence: true

  #
  #
  # Stores
  # --------------------------------------------------------------------------------------
  #
  #
  #


  #
  #
  # Instance Methods
  # --------------------------------------------------------------------------------------
  #
  #
  #


  #
  #
  # Class Methods
  # --------------------------------------------------------------------------------------
  #
  #
  #


  #
  #
  # Private Methods
  # --------------------------------------------------------------------------------------
  #
  #
  #

  private

  def ensure_uuid
    self.uuid ||= SecureRandom.uuid
  end

  def ensure_mood
    self.mood ||= "none"
  end
end
